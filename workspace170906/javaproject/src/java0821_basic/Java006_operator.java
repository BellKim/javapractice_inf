package java0821_basic;

public class Java006_operator {

	public static void main(String[] args) {
		int a=10;
		int b=3;
		
		System.out.println(a/b);	//3 (int / int)
		System.out.println(a%b);	//3 (int / int)
		System.out.println((double)(a/b));	//3.0
		System.out.println((double)a/b);	//3.333333
		
		
		


		
		
		
		
		
		
		
		

	}//end of main

}//end of class
