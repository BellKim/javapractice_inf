package jaesik;

public class hello {

	public static void main(String[] args) {
		for(int j=1; j<=3; j++) {
			for(int i=1; i<=j; i++) {
				System.out.print("*");
			}
			System.out.println(" ");
		}
		for(int j=2; j<=1; j--) {
			for(int i=2; i<=j; i--) {
				System.out.print("*");
			}
			System.out.println(" ");
		}
		


	}

}
