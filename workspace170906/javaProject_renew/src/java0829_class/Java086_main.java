package java0829_class;

//java Java086_main 홍길동 이영희 깜순이
public class Java086_main {

	public static void main(String[] args) {
		System.out.println(args[0]);
		/*콘솔창 - run as - config 누르고 메인탭에서  mainclass 를 현재파일 선택. 
		 * argument 탭에서 첫번째 입력창에 홍길동 입력
		 * 콘솔창에서 홍길동 뜨는지확인.  
		 */
		
		for(String data : args)
			System.out.println(data);
		
		
		
		
		

	}

}
